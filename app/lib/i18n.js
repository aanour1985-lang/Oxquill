// ─── HELPER: Translate function ───────────────────────────
export function t(key, rtl) {
  const entry = DICT[key];
  if (!entry) return key;
  return rtl ? entry.ar : entry.en;
}

// ─── HELPER: Create bound translator with rtl ─────────────
export function makeT(rtl) {
  return function(key) {
    return t(key, rtl);
  };
}
